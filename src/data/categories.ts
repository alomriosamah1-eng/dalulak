export const allCategories: Category[] = [...categories, ...subcategories, ...deepSubcategories];

export function getCategoryById(id: string): Category | undefined {
  return allCategories.find(c => c.id === id);
}

export function getCategoryPath(categoryId: string): string[] {
  const cat = getCategoryById(categoryId);
  return cat?.path || [categoryId];
}

export function getCategoryNameAr(categoryId: string): string {
  return getCategoryById(categoryId)?.nameAr || categoryId;
}

export function getRootCategories(): Category[] {
  return categories;
}

export function getChildren(parentId: string): Category[] {
  return allCategories.filter(c => c.parentId === parentId);
}
